const rspNav = () => {
    let brg = document.getElementsByClassName('burger')[0]
    let ul = document.getElementsByClassName('nav-ul')[0]
    let rightNav = document.getElementsByClassName('nav-search-bar')[0]
    let nav = document.getElementsByTagName('nav')[0]
    brg.addEventListener('click',function(){
        ul.classList.toggle('rsp');
        rightNav.classList.toggle('rsp');
        nav.classList.toggle('rsp-nav')
        nav.classList.toggle('nav-h-rsp')
    })
}
rspNav();
const cardAnimattion = () => {
    const cards = document.querySelectorAll('.myAnimate');
    const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible'); 
        observer.unobserve(entry.target);
      }
    });
    }, { threshold: 0.3 });
    cards.forEach(card => {
      observer.observe(card);
    });  
}
cardAnimattion();
const createCard = (item)  => {
    let cardWrp = document.createElement('div');
    cardWrp.classList = 'clm myAnimate';
    cardWrp.innerHTML = `
    <div class="card">
        <img class="card-img" height="238" width="263" src="${item.urlTOImage || 'fallback-image.jpg'}" alt="card-image">
        <h2 class="card-title">${item.title}</h2>
        <button class="card-btn">Order Now</button>
    </div>
    `;
     return cardWrp;
}

