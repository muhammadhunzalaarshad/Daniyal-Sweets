const searchItem = (query) => {
    let resultWrapper = document.getElementsByClassName('clm-wrp')[0];
    resultWrapper.innerHTML = '';
    let allItems = sweet.concat(bakers, cakes, roll);
    console.log()
    query = query.toLowerCase().trim();
    let filterItems = allItems.filter(item => item.title.toLowerCase().includes(query));
    if (filterItems.length === 0) {
        resultWrapper.innerHTML = '<p>No results found</p>';
        return;
    }
    filterItems.forEach(item => resultWrapper.appendChild(createCard(item)));
    cardAnimattion();

}
 
